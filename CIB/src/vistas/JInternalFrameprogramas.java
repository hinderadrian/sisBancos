    public void cargarcbo() {

        this.cbofacultad.removeAllItems();

        try {

            st = cn.createStatement();
            rs = st.executeQuery("select * from facultad");
            cbofacultad.addItem("");
            while (rs.next()) {
                this.cbofacultad.addItem(rs.getString("nombfacultad"));
            }

        } catch (Exception e) {

        }

    }

