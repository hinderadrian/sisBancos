    private void btnins_programaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnins_programaActionPerformed

        int posicioncbo = cbofacultad.getSelectedIndex();
        String codigo = txtcodprog.getText();
        String programa = txtprograma.getText();
        String ref = txtref.getText();

        if (posicioncbo != 0 && codigo != null && programa != null && ref != null) {
            p.registrarprograma(codigo, idfacultad, programa, ref);

            verprogramas();

        } else {
            JOptionPane.showMessageDialog(null, "No ha suministrado toda la informacion");

        }
    }//GEN-LAST:event_btnins_programaActionPerformed

